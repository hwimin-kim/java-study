package chapter04;

public class StringTest01 {

	public static void main(String[] args) {
		// c:\temp
		System.out.println("c:\\temp");
		
		// "hello"
		System.out.println("\"hello\"");
		
		// 제어문자
		// \t: tap
		// \r: carrige return
		// \n: newline
		System.out.print("hellow\tworld\n");
		System.out.println("hellow\tworld");
		
		// '
		char c = '\'';
		System.out.println(c);
	}

}
